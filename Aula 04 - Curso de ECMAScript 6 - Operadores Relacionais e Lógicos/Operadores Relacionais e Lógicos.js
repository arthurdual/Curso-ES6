let x = 4;
let y = 2;
//operadores relacionais
console.log(x > y); //maior
console.log(x < y); //menor
console.log(x >= y); //maior ou igual
console.log(x <= y); //menor ou igual
console.log(x == y); //igual //o valor deve ser igual
console.log(x != y); //Diferente

console.log(x === y); //identico //o valor e o tipo de variavel deve ser igual
console.log(x !== y); //diferente //o valor e o tipo de variavel deve ser diferentes


//Operadores logicos
//OU / ||
console.log(x > 3);
console.log(y < 2);
console.log(x > 3 || y < 2);
//apenas uma das condiçõesprecisar ser verdadeira para retornar verdadeiro

// E / &&
console.log(x > 3);
console.log(y < 2);
console.log(x > 3 && y < 2);
//As duas condições precisa ser verdadeiras para retornar verdadeiro


//! /NOT
console.log(!true);//retor false pois NÃO é true (!true)
console.log(!false);//retor true pois NÃO é false (!false)


